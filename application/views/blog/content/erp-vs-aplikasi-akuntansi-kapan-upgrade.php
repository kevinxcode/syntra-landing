<h2 id="cara-migrasi">Cara Migrasi Tanpa Mengganggu Operasional</h2>

<h3>Fase 1: Persiapan (2–4 minggu)</h3>
<ul>
  <li>Audit proses bisnis saat ini, identifikasi 3–5 pain point utama.</li>
  <li>Bersihkan master data (pelanggan, supplier, produk).</li>
  <li>Tentukan PIC migrasi internal.</li>
</ul>

<h3>Fase 2: Setup & Konfigurasi (3–6 minggu)</h3>
<ul>
  <li>Vendor setup environment, konfigurasi modul sesuai workflow Anda.</li>
  <li>Migrasi master data (chart of accounts, kontak, produk, stok awal).</li>
  <li>Training admin level 1.</li>
</ul>

<h3>Fase 3: Parallel Running (4 minggu)</h3>
<p>Jalankan sistem lama dan ERP baru bersamaan selama 1 bulan. Bandingkan output keduanya, identifikasi gap. Ini fase paling penting — jangan dipotong.</p>

<h3>Fase 4: Cutover (1 hari)</h3>
<p>Sistem lama dimatikan, ERP jadi single source of truth. Pilih akhir bulan agar laporan rapi.</p>

<h3>Fase 5: Optimasi (3–6 bulan)</h3>
<p>Aktifkan modul lanjutan: forecasting, dashboard manajemen, integrasi marketplace. Fase ini sering dilupakan tapi inilah saatnya ROI maksimal terealisasi.</p>

<p>Jika Anda mengangguk pada 3 atau lebih sinyal di atas, pertanyaannya bukan "apakah kami butuh ERP?" — tapi "berapa lama lagi kami menunda sebelum kerugian operasional melebihi biaya implementasi?"</p>
